USE <nome do banco de dados> - para entrar dentro do banco selecionado

SHOW DATABASES -para mostrar todos os banco de dados existentes

SHOW TABLES - para listar as tabelas existentes

SELECT * FROM <nome do tabela> - para listar as colunas e os dados da tabela

SELECT <nome da coluna> FROM <nome da tabela> - para listar os dados da coluna escolhida de certa tabela

DESCRIBE <nome da tabela> - para listar os componentes da tabela selecionada